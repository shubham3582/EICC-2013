  EICC 2013 stands for 2<sup>nd</sup> National  Conference on Information Technology. It is the premier forum for Theory, Development, Applications, Experiences, and Evaluation of Information Technology. It enables the  international scientific elite, including experienced scientists, promising young researchers and practicians alike  to gather together to discuss their views and research findings.  
  EICC 2013 promotes fundamental and applied  research continuing advanced academic education and transfers knowledge between involved both sides of and the application of Information Technology.<br><br />
  This conference will be a platform for distinguished scholars to explore a cutting-edge Information Technology topic. Friendly atmosphere at the conference will promote culture interchange and develop friendship among the partcipants. 
