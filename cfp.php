  <strong>Objectives and Vision</strong>&nbsp;&nbsp;&nbsp;&nbsp;
  <br><br>
  The objective of EICC 2013 is to provide a forum for ideas on Information Technology, including advances in information technology, industrial, service, management issues etc.  
  <br><br>
  EICC 2013 has brought together software engineers, developers, performance analysts and software/performance modelers who are addressing the challenges of increasing system complexity, rapidly evolving information technologies, short time to market, incomplete documentation, and less-than-adequate methods, models and tools for developing, modeling, and measuring scalable, high-performance applications in Information Technology. The focus of EICC 2013 is therefore in the Multidisciplinary and Hybrid/Convergent research rather than one discipline in isolation.
  <br><br>
  It is generally acknowledged that the advancement of technologies has improved the quality of our life and the technologies enabled pervasive access for everyone with ubiquitous devices to ensure a connected global world, global-village. However, current technological solutions, no matter how advanced, by themselves are not enough without taking into account the human beings who actually use them. 
  <br><br>
  The organizers of EICC 2013 believe that Multidisciplinary and Hybrid/Convergent research is the most important aspect of success and it will potentially result in even more exciting developments.
  Interaction Science is one of the primary prospects for novel and future oriented research. We are genuinely aimed at supporting further research and progress of technologies in Information Technology.
  EICC 2013 welcomes broad genuine ideas aimed at the development of Multidisciplinary and Hybrid/Convergent research in Information Technology. It is our Vision that with the help of this conference we will be able to build sufficient grounds for fulfillment of most brilliant and far-reaching ideas in Information Technology. Based on our previous experiences, we are sure that discussions fueled by this conference will enrich the participants with precious knowledge and experiences. 
  <br><br>
  <strong>  Mission and Aims </strong>
  <br><br>EICC 2013 will trigger off the scientific community to propose topics that should be tackled from research perspective and let the community explain how to best use their tools for practical and theoretical problems of Information Technology. Many various contributions are foreseen from prospective authors. This includes use-cases of theoretical tools and methods to solve practical problems. Such contributions should be as usable as possible by practitioners in the related field. We also expect research results from practitioners that have identified a problem that could be solved by tools from network sciences. One of the missions of EICC 2013 is to make the scientific community aware of the importance of the issues in Information Technology and to suggest means by which the problem may be solved by the scientific community. The contributions should stimulate interaction between theoreticians and practitioners and also have high potential impact in either field.
  <br><br>
  We aims to bring together and share brilliant and ideas, accumulated knowledge and unique experiences for mutual benefit of researchers and practitioners and Xplore possible directions for development of Multidisciplinary and Hybrid/Convergent research in the areas of Information Technology. 
